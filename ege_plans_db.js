export const getPlanById = (id) => RAW_PLANS.find(p => p.id === id) ?? null;
export const searchPlans = (query) => {
  const q = query.toLowerCase().trim();
  if (!q) return RAW_PLANS;
  return RAW_PLANS.filter(p =>
    p.title.toLowerCase().includes(q) ||
    p.aliases.some(a => a.toLowerCase().includes(q)) ||
    p.referencePlan.toLowerCase().includes(q)
  );
};
export const getDifficulty = (plan) => {
  const n = plan.mandatoryPoints.length;
  if (n <= 2) return "easy"; if (n === 3) return "medium"; return "hard";
};
export const getRandomPlan = (blockId = null) => {
  const pool = blockId ? getPlansByBlock(blockId) : RAW_PLANS;
  return pool[Math.floor(Math.random() * pool.length)] ?? null;
};
export const getRandomPlanExcluding = (excludeIds = [], blockId = null) => {
  const pool = (blockId ? getPlansByBlock(blockId) : RAW_PLANS).filter(p => !excludeIds.includes(p.id));
  if (!pool.length) return getRandomPlan(blockId);
  return pool[Math.floor(Math.random() * pool.length)];
};
